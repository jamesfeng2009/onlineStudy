    with open(SRC, "r", encoding="utf-8") as f:
        data = json.load(f)

    for dlg in data:
        cid = dlg["conversation_id"]
        if cid not in T:
            print(f"WARNING: No translations for {cid}")
            continue
        for utt in dlg["utterances"]:
            idx = utt["index"]
            if idx in T[cid]:
                utt["text"] = T[cid][idx]
            else:
                print(f"WARNING: No translation for {cid} index {idx}")

    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Written {len(data)} dialogues to {OUT}")


