def main():
    with open(SRC, 'r', encoding='utf-8') as f:
        data = json.load(f)

    src_total = 0
    out_total = 0
    missing = []
    for dlg in data:
        cid = dlg['conversation_id']
        for utt in dlg['utterances']:
            src_total += 1
            key = (cid, utt['index'])
            if key in T:
                utt['text'] = T[key]
                out_total += 1
            else:
                missing.append(key)

    if missing:
        print('MISSING translations for:', missing)
        raise SystemExit(1)

    # Verify counts match
    assert src_total == out_total, f'count mismatch: src={src_total} out={out_total}'

    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f'Done: {len(data)}/{len(data)} dialogues translated, {out_total} utterances, written to {OUT}')


if __name__ == '__main__':
    main()